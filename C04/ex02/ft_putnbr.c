#include	<unistd.h>

void	ft_putnbr(int nb)
{
	long int	i;
	char		number;

	i = nb;
	if (i < 0)
	{
		write (1, "-", 1);
		i = i * -1;
	}
	if (i >= 10)
	{
		ft_putnbr(i / 10);
	}
	number = (i % 10) + '0';
	write (1, &number, 1);
}
